#include "libft.h"

int		ft_atoi(const char *str)
{
	char	*s;
	int		num;
	int		sign;

	s = (char *)str;
	num = 0;
	while (*s == ' ' || *s == '\n' || *s == '\t' || *s == '\r' || *s =='\f'
			|| *s == '\v')
		s++;
	sign = (*s == '-') ? -1 : 1;
	s = (*s == '+' || *s == '-') ?  s + 1 : s;
	while (*s >= '0' && *s <= '9')
	{
		num = num * 10 + *s - 48;
		s++;
	}
	num *= sign;
	return (num);
}
